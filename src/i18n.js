  /* ── Init ────────────────────────────────────────────────── */
  const savedLang = localStorage.getItem('spotme-language') || 'en';

  i18next.init({
    lng: savedLang,
    fallbackLng: 'en',
    resources,
    initImmediate: false,
    interpolation: { escapeValue: false },
  });

  function applyLangDir(code) {
    const lang = LANGUAGES.find(l => l.code === code);
    document.documentElement.lang = code;
    document.documentElement.dir = (lang && lang.rtl) ? 'rtl' : 'ltr';
  }
  applyLangDir(savedLang);

  /* ── Public API ──────────────────────────────────────────── */
  function t(key, opts) {
    return i18next.t(key, opts);
  }

  function changeLanguage(code) {
    i18next.changeLanguage(code);
    localStorage.setItem('spotme-language', code);
    applyLangDir(code);
    window.dispatchEvent(new Event('spotme:lang-change'));
  }

  /* ── React hook ──────────────────────────────────────────── */
  function useTranslation() {
    const [, forceUpdate] = useState(0);
    useEffect(() => {
      function handler() { forceUpdate(n => n + 1); }
      window.addEventListener('spotme:lang-change', handler);
      return () => window.removeEventListener('spotme:lang-change', handler);
    }, []);
    return { t };
  }

  SpotMe.i18n = {
    t,
    changeLanguage,
    get currentLang() { return i18next.language || savedLang; },
    LANGUAGES,
  };
  SpotMe.useTranslation = useTranslation;
